    private static int Map(ref string s)
    {
        // rock A/X
        // paper B/Y
        // scissors C/Z
        return s switch
        {
            "A X" => 1 + 3,
            "A Y" => 2 + 6,
            "A Z" => 3 + 0,
            "B X" => 1 + 0,
            "B Y" => 2 + 3,
            "B Z" => 3 + 6,
            "C X" => 1 + 6,
            "C Y" => 2 + 0,
            "C Z" => 3 + 3,
            _ => throw new ArgumentOutOfRangeException(s)
        };
    }

    private static int Map2(ref string s)
    {
        // rock A/X loose
        // paper B/Y draw
        // scissors C/Z win
        return s switch
        {
            "A X" => 3 + 0,
            "A Y" => 1 + 3,
